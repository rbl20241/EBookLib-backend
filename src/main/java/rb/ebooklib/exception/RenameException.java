package rb.ebooklib.exception;

public class RenameException extends Exception {

    public RenameException() {
    }

    @SuppressWarnings("unused")
    public RenameException(String arg0) {
        super(arg0);
    }

    @SuppressWarnings("unused")
    public RenameException(Throwable arg0) {
        super(arg0);
    }

    @SuppressWarnings("unused")
    public RenameException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    @SuppressWarnings("unused")
    public RenameException(String arg0, Throwable arg1, boolean arg2,
                           boolean arg3) {
        super(arg0, arg1, arg2, arg3);
    }

}
